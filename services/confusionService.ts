/**
 * 헷갈림 모드(Confusion Mode) 전용 로직 서비스
 * 일반 모드와 달리 오답을 유도하고 선지를 비트는 로직을 담당합니다.
 */

export const getConfusionModePrompt = (): string => {
  return `
    [🔥🔥 헷갈림 모드 (Confusion Mode) - 악랄한 함정 설계]:
    이 모드의 목표는 대충 아는 학생을 틀리게 만드는 것입니다. 문제가 어려운 게 아니라, **선지가 매우 헷갈려야** 합니다.
    
    1. **단어 교체 트릭 (Word Swapping) - 필수 적용**: 
       - 문장에서 핵심이 되는 명사나 형용사를 반대 개념이나 비슷한 다른 단어로 교묘하게 바꾸십시오.
       - 예시: "내부(Internal)" ↔ "외부(External)"
       - 예시: "송신(Transmit)" ↔ "수신(Receive)"
       - 예시: "증가(Increase)" ↔ "감소(Decrease)"
       - 예시: "정적(Static)" ↔ "동적(Dynamic)"
       - 예시: "TCP" ↔ "UDP", "L3" ↔ "L2", "허용(Allow)" ↔ "차단(Deny)"
    
    2. **서술어 비틀기**: 
       - "~할 수 있다(Can)"를 "~해야만 한다(Must)"로 바꾸어 예외 상황을 건드리십시오.
       - "모든(All)"을 "일부(Some)"로 바꾸거나 그 반대로 서술하십시오.
    
    3. **꼬리 자르기**: 
       - 선지의 앞부분 80%는 완벽하게 맞는 말이지만, 마지막 서술어만 살짝 바꾸어 틀린 문장으로 만드십시오.
       - 학생이 끝까지 읽지 않으면 무조건 틀리게 설계하십시오.

    [출제 원칙]:
    - 정답 선지는 논리적으로 완벽해야 하지만, 오답 선지와 텍스트 유사도가 90% 이상이어야 합니다.
    - 단순히 "없다"를 "있다"로 바꾸는 수준을 넘어, 개념을 혼동하게 만드십시오.
  `;
};

export const getNormalModePrompt = (): string => {
  return `
    [일반 모드]:
    - 학습 자료의 핵심 원리를 묻는 정석적인 문제를 만드십시오.
    - 단순한 단어 찾기보다는 상황 판단이나 개념 적용 문제를 만드십시오.
    - 정답과 오답의 구분이 명확해야 합니다.
  `;
};
